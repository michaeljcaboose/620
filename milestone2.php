                    <h3>Brief Summaries of the Interviews</h3>
                    <div>
                        <h4>Matthew</h4>
                        <p>I conducted my interviews in informal and private settings using the Voice Memo app on my
                            phone. I wanted to be able to focus on driving the interview rather than having to split my
                            attention between taking notes and asking questions. This allowed for a better back-and-forth
                            dynamic where I could expand upon the responses I was getting from my interview subjects.
                            The interviews lasted between 10 to 20 minutes. I'd wait until hours after the interview to
                            go back and listen. I would then summarize the details of the interview into finer points for
                            our interpretation session. </p>
                    </div>
                    <div>
                        <h4>Aishwarya</h4>
                        <p>Conducted three interviews of graduate students. The interviews were documented through an
                            audio recorder and span between 17 to 45 minutes. There were instances when the conversation
                            felt to be drifting away from the topic of focus but constant and improvised refinement of
                            certain questions specific to the user provided really useful perspectives. I prefered not
                            to take notes during the interview and saved it for later when I heard the recordings again.
                            The three users provided a varying degree of perspectives - while one preferred to order
                            together, the other was fine with people ordering their food separately in a social setting.
                            They unanimously agreed on the fact that ordering and deciding upon a place to eat takes the
                            most time and gets even more confusing when views vary and there is a constraint on ordering
                            from a particular place.
                        </p>
                    </div>
                    <div>
                        <h4>Sovantharith</h4>
                        <p>I conducted my interviews in person at the Center for Cybersecurity, where my office is
                            located. My participants were my labmates. Two of them were males and one was female. The
                            conversations were all recorded on the Voice Memo app on my smartphone. The interview sessions
                            lasted about 10 minutes on average. The interviews were conducted on 09/25 and 09/26. I
                            produced the notes from the interview on 09/27 for the interpretation session.</p>
                        <p>The interview
                            sessions were quite short because the interviewees didn’t have a lot of experience or thought
                            that they want to share. They didn’t think that they have a lot of interesting details to share.
                            I was unsuccessful in guiding them to speak more about their experience without leading them
                            to the answers that I want. However, there is a consensus that in a group setting, it is hard
                            to decide where to eat or what to order. They also all agree that wait time at the restaurant
                            is the main irritant about going out to eat. Last but not least, one interesting observation
                            is that the interviewees had different views about pictures on a menu; one person thought that
                            it is not classy to have pictures on a menu; one person thought that the pictures should always
                            be included; the last interviewee was indifferent.</p>
                    </div>
                    <div>
                        <h4>Peter</h4>
                        <p>I conducted my interviews in open space setting and online.  The first participant was Deaf
                            and I asked him for a permission to have a video recorded.  I clarified the questions to him
                            using visual American Sign Language.  That interview session lasted about 20 minutes.  I
                            translated from ASL to written English into the notes after the interview ended.  For the
                            online interviews, the other two participants were extremely busy and had conflict with
                            schedule. Two participants had a lot of experience eating out and eating inside the restaurant.
                            I shared them interview questions through the Google Document.  They were typing their
                            responses while I followed them up with the questions to explain more about their experience.
                        </p>
                    </div>
