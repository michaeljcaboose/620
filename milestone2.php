                <div class="milestone-item">
                    <h3>Interpretation Session</h3>
                    <p>Some problems that we did notice during the interviews,</p>
                    <ul>
                        <li>
                            From the subjects of Peter, the requirements at least have some features which let the customers
                            communicate that they are Deaf.
                        </li>
                        <ul>
                            <li>
                                Deaf people prefer the iPad or smartphones to place their orders to be sent to the system
                                instead of showing and/or pointing the entree from the menu.
                                It also applies to the people with disabilities.
                            </li>
                        </ul>
                        <li>Phone ordering tends to be most hectic as most places are busy around the time (lunch/dinner)
                            - noise, operator temperament, accent etc. also play a role in effective communication.
                            Wrong orders are a common result of this.
                        </li>
                        <li>Unknown ingredients could be better represented or presented in the simplest visual-textual manner.</li>
                        <li>Interpersonal dynamics influence decision making and also vary with the formality of the situation.
                        </li>
                        <li>Most time gets consumed in decision making regardless of whether the group is eating out or
                            ordering in. This further increases when people are eating out, because after deciding on
                            the place they must decide on the menu.
                        </li>
                        <li>
                            While using apps when ordering, time gets wasted when looking up details on food or for coupons.
                        </li>
                        <li>People dislike the fact that on certain apps, the must jump from the medium of a phone
                            screen to a call - it is disruptive to the flow.
                        </li>
                        <li>Allergies, foreign ingredients and other dietary restrictions need to be made clear through
                            check boxes or comments - some places do not cater to these preferences and this info is
                            made available at a later stage - on phone call etc.
                        </li>
                        <li>Closing times also affect these decisions, especially when orders are placed during parties
                            which are usually at night. Quick alternatives need to be figured out or plans need to be
                            dropped because the place of choice closes down at a particular hour.
                        </li>
                    </ul>
